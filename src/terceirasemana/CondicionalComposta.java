package terceirasemana;

public class CondicionalComposta {
    /**
     * @param args
     */
    public static void main(String[] args) {
        int nota = 8;

        if (nota < 7) {
            System.out.println("Reprovado");
        }
        else 
            System.out.println("Aprovado");
        
    }
    
}
