package terceirasemana;

public class BreakContinue {    
    public static void main(String[] args) {
        for(int x = 0; x <= 5; x++){
            if(x == 3)
                continue;//break para no 2 e continue pula a exibição do 3 porque pula o sysout abaixo
                System.out.println(x);
            
        }
    }
    
}
